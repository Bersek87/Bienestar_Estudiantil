    private void generarReporteAlumno() {
     try {  
         String sql, ag1="", ag2="", ag3="";
         sql="SELECT noCuenta as 'Numero de Cuenta', concat(nombreAlumno,\" \", apellidoAlumno) as 'Nombre Completo', carreraAlumno as 'Carrera', emaiAlumno as 'Correo ELectronico', telefono as 'No Telefonico', lugar_proc_alumno as 'Direccion' from Alumno";
         
         if(ch_anio1.isSelected()==true ||ch_Lugar.isSelected()==true ||ch_carrera.isSelected()==true ){        
         
             if (ch_anio1.isSelected()==true){
         
             ag1=", `noCuenta` like '"+Jch_anio.getYear()+"%' ";
         
            }
            if (ch_Lugar.isSelected()==true){ 
            ag2=", lugar_proc_alumno like '%"+txt_lugar.getText()+"%' ";
            }
            if (ch_carrera.isSelected()==true){ 
            ag3=", carreraAlumno like '"+cb_carrera.getSelectedItem().toString()+"' ";
         }
         sql=sql+" where concat_ws('and'"+ag1+ag2+ag3+")";
             
         }           
          
         operaciones op = new operaciones();
            
            
            
            ResultSet rs = op.ConsultaBase(sql);                  
            
            
            ResultSetMetaData Columnas = rs.getMetaData();
            
            DefaultTableModel alumno=new DefaultTableModel();
            int cantidadColumnas = Columnas.getColumnCount();
            for (int i = 1; i <= cantidadColumnas; i++) {
                    alumno.addColumn(Columnas.getColumnLabel(i)); //Agrega columnas al modelo con el titulo extraido de la metadata
             }
             //Extrae las filas del resultSet y los para al modelo
                while (rs.next()) {
                       Object[] fila = new Object[cantidadColumnas];
                        for (int i = 0; i < cantidadColumnas; i++) {
                                 fila[i]=rs.getObject(i+1);
                        }
                 alumno.addRow(fila); //agregamos al modelo la fila extraida.
                }
                
            tabla_alumno.setModel(alumno);
            
        } catch (SQLException ex) {
            Logger.getLogger(Denuncias.class.getName()).log(Level.SEVERE, null, ex);
        }
       }

